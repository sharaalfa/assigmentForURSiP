package org.sha.ursip.service;



public class WriteString {
    private String name;
    private int officialText;
    private int engText;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getOfficialText() {
        return officialText;
    }

    public void setOfficialText(int officialText) {
        this.officialText = officialText;
    }

    public int getEngText() {
        return engText;
    }

    public void setEngText(int engText) {
        this.engText = engText;
    }


}
